class RelationalOperators
  {
    public static void main(String args[])
    {
      System.out.println("23<40 is "+(23<40));
      System.out.println("23>40 is "+(23>40));
      System.out.println("23>=40 is "+(23>=40));
      System.out.println("23<=40 is "+(23<=40));
      System.out.println("23==40 is "+(23==40));
      System.out.println("23!=40 is "+(23!=40));
     }
    
  }