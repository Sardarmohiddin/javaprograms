class Percentage
  {
    public static void main(String args[])
    {
      float sub1=95f,sub2=84f,sub3=85f,sub4=85f,sub5=93f,Obt_marks,total_marks=500f;
      float percentage;
      Obt_marks=(sub1+sub2+sub3+sub4+sub5);
      percentage=(Obt_marks/total_marks)*100;
      System.out.println("Percentage of the student is "+percentage);
    }
  }